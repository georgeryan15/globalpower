export const getEmissionsColor = (emissions: number): string => {
  // Pastel color scale from light green (low) to dark brown (high)
  if (emissions <= 50) return "#a8e6cf"; // Very light pastel green
  if (emissions <= 100) return "#b8e6c0"; // Light pastel green
  if (emissions <= 150) return "#c8e6b0"; // Pastel green
  if (emissions <= 200) return "#d8e6a0"; // Yellow-green
  if (emissions <= 300) return "#e8e690"; // Pastel yellow
  if (emissions <= 400) return "#f0d890"; // Pastel orange-yellow
  if (emissions <= 500) return "#e8c080"; // Pastel orange
  if (emissions <= 600) return "#dca870"; // Light brown
  if (emissions <= 700) return "#d09060"; // Medium brown
  if (emissions <= 800) return "#c47850"; // Brown
  if (emissions <= 900) return "#b86040"; // Dark brown
  return "#a04830"; // Very dark brown
};

