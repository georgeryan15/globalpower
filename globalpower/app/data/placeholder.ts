export interface PowerPlant {
  id: string;
  name: string;
  country: string;
  latitude: number;
  longitude: number;
  type: "coal" | "gas" | "nuclear" | "hydro" | "wind" | "solar" | "oil";
  capacity: number; // MW
  output: number; // MW (current)
  emissions: number; // gCO2/kWh
  constructionYear: number;
  owner: string;
  status: "operating" | "maintenance" | "offline";
  hourlyOutput: number[]; // Last 24 hours
}

export interface CountryEmissions {
  countryCode: string;
  name: string;
  emissions: number; // gCO2/kWh average
  totalCapacity: number; // MW
  renewablePercent: number;
}

const generateHourlyOutput = (base: number, variance: number = 0.1): number[] => {
  return Array.from({ length: 24 }, () =>
    Math.round(base * (1 + (Math.random() - 0.5) * variance))
  );
};

